#OPERADORES

#Operadores Aritmeticos
print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)
print(10 % 4) #modulo
print(8 // 4) #aproximar la division por lo tanto siempre sera un numero entero
print(2 ** 3) #calcular exponente
print(2 ** 3 + 3 - 7 / 1 // 4) 


print("hola" + "pyhton")
print("hola" + str(5))
print("hola" * 5)

#Operadores Comparativos
print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 !=  4)
print("aaaa" >= "abaa") #Compara el orden alfabetico (Tiene encuenta las mayusculas)
print(len("aaaa") >= len("aaaa"))#Compara cantidad de caracteres

#Operadores logicos
print(3 < 4 and "hola" > "python") #Se cumplen ambas condiciones
print(3 < 4 or "hola" > "python") #Se cumple una u otra condicion
print(not(3 < 4)) #No se cumpla esta condicion






